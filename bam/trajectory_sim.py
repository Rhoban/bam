"""Simulate a trajectory from :mod:`bam.trajectory` with an identified model.

Unlike ``bam.plot``, this is a pure simulation: no log is replayed, the goal
positions are generated by the trajectory itself. This is a debug script, the
setup below is meant to be edited by hand.

Usage::

    python -m bam.trajectory_sim --params params/xl330/m6.json

Several params files can be passed, they are then simulated on the same
trajectory and overlaid on the plot for comparison::

    python -m bam.trajectory_sim --params params/xl330/m4.json params/xl330/m6.json
"""

import argparse
import numpy as np
import matplotlib.pyplot as plt

from .model import load_model
from . import simulate
from . import trajectory as trajectory_module

# ── Setup, edit me ────────────────────────────────────────────────────────────
# Trajectory to simulate (see bam/trajectory.py)
trajectory = trajectory_module.HalfSine()

# Pendulum testbench: mass at the tip [kg], arm mass [kg] and arm length [m]
mass = 0.15
arm_mass = 0.0
length = 0.1

# Firmware P-gain and supply voltage
kp = 125
vin = 7.4

# Simulation timestep [s]
dt = 0.005
# ──────────────────────────────────────────────────────────────────────────────

arg_parser = argparse.ArgumentParser()
arg_parser.add_argument("--params", type=str, required=True, nargs="+")
args = arg_parser.parse_args()

models = [load_model(params) for params in args.params]

# Legend labels: model names (m4, m6, ...) unless two params files share the same
# model, in which case the paths are used to tell them apart.
names = [model.name for model in models]
if len(set(names)) != len(names):
    names = list(args.params)

# The trajectory is turned into a log-like dict, so that the very same rollout
# used against real logs can be reused here.
ts = np.arange(0.0, trajectory.duration, dt)
entries = []
for t in ts:
    goal_position, torque_enable = trajectory(t)
    entries.append(
        {
            "position": 0.0,
            "speed": 0.0,
            "goal_position": goal_position,
            "torque_enable": torque_enable,
            "timestamp": t,
        }
    )

log = {
    "mass": mass,
    "arm_mass": arm_mass,
    "length": length,
    "kp": kp,
    "vin": vin,
    "motor": models[0].actuator_name,
    "trajectory": type(trajectory).__name__,
    "dt": dt,
    "entries": entries,
}

all_sim_q = []
all_sim_speeds = []
all_sim_controls = []
for model in models:
    simulator = simulate.Simulator(model)
    sim_q, sim_speed, sim_controls = simulator.rollout_log(log, simulate_control=True)
    all_sim_q.append(sim_q)
    all_sim_speeds.append(sim_speed)
    all_sim_controls.append(sim_controls)

# There is no ground truth here (pure simulation), so the first params file is
# taken as the reference the others are compared against.
if len(models) > 1:
    print(f"MAE against {names[0]} (reference)")
    for name, sim_q, sim_speed in zip(names[1:], all_sim_q[1:], all_sim_speeds[1:]):
        mae_q = np.mean(np.abs(np.array(sim_q) - np.array(all_sim_q[0])))
        mae_speed = np.mean(np.abs(np.array(sim_speed) - np.array(all_sim_speeds[0])))
        print(f"  {name}: q {mae_q:.6f} rad, speed {mae_speed:.6f} rad/s")

goal_q = [entry["goal_position"] for entry in entries]
torque_enable = np.array([entry["torque_enable"] for entry in entries])
control_unit = models[0].actuator.control_unit()

f, (ax1, ax2, ax3) = plt.subplots(3, sharex=True)

for name, sim_q in zip(names, all_sim_q):
    ax1.plot(ts, sim_q, label=f"{name}_q")
ax1.plot(ts, goal_q, label="goal_q", color="black", linestyle="--")
ax1.set_title(
    f"{log['motor']}, {log['trajectory']}, m={mass}, l={length}, k={kp} [simulation]"
)
ax1.set_ylabel("angle [rad]")
ax1.grid()
ax1.legend()

for name, sim_speed in zip(names, all_sim_speeds):
    ax2.plot(ts, sim_speed, label=f"{name}_speed")
ax2.set_ylabel("speed [rad/s]")
ax2.grid()
ax2.legend()

for name, sim_controls in zip(names, all_sim_controls):
    ax3.plot(ts, sim_controls, label=f"{name}_{control_unit}")
all_controls = [0.0 if c is None else c for cs in all_sim_controls for c in cs]
# Shading the areas where torque is False
ax3.fill_between(
    ts,
    min(all_controls) - 0.02,
    max(all_controls) + 0.02,
    where=[not torque for torque in torque_enable],
    color="red",
    alpha=0.3,
    label="torque off",
)
ax3.set_ylabel(f"{control_unit}")
ax3.grid()
ax3.legend()

plt.xlabel("time [s]")
plt.show()
